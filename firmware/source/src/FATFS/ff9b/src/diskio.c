/*-----------------------------------------------------------------------*/
/* Inidialize a Drive                                                    */
/*-----------------------------------------------------------------------*/

DSTATUS disk_initialize (
	BYTE pdrv				/* Physical drive nmuber (0..) */
)
{
	DSTATUS stat;
	switch (pdrv) {
	case MMC :
		stat = MMC_disk_initialize();
		// translate the reslut code here
		return stat;
	}
	return STA_NOINIT;
}



